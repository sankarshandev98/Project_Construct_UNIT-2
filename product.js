  document.querySelector("select").addEventListener("change", sorting);

  function sorting() {
    let sortElement = document.querySelector("#sorting").value;
    if (sortElement == "low") {
      productDetails.sort(function (a, b) {
        return a.price - b.price;
      });
    }

    if (sortElement == "high") {
      productDetails.sort(function (a, b) {
        return b.price - a.price;
      });
    }

    if (sortElement == "A") {
      productDetails.sort(function (a, b) {
        let aName = a.name.toUpperCase();
        let bName = b.name.toUpperCase();
        if (aName < bName) {
          return -1;
        }
        if (aName > bName) {
          return 1;
        }
        if (aName == bName) {
          return 0;
        }
      });
    }
    if (sortElement == "Z") {
      productDetails.sort(function (a, b) {
        let aName = a.name.toUpperCase();
        let bName = b.name.toUpperCase();
        if (aName > bName) {
          return -1;
        }
        if (aName < bName) {
          return 1;
        }
        if (aName == bName) {
          return 0;
        }
      });
    }

    displayProduct(productDetails);
  }
